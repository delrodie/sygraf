    private $profession;

    /**
     * @var string
     *
     * @ORM\Column(name="domicile", type="string", length=255, nullable=true)
     */
    private $domicile;

    /**
     * @var string
     *
     * @ORM\Column(name="classe", type="string", length=1, nullable=true)
     */
    private $classe;

    /**
     * Un chef appartient a une region
     *
     * @ORM\ManyToOne(targetEntity="AppBundle\Entity\Region", inversedBy="chefs")
     * @ORM\JoinColumn(name="region", referencedColumnName="id", nullable=true)
     */
    private $region;

    /**
     * NOTE: This is not a mapped field of entity metadata, just a simple property.
     *
     * @Vich\UploadableField(mapping="chef_image", fileNameProperty="imageName", size="imageSize", nullable=true)
     *
     * @var File
     */
    private $imageFile;

    /**
     * @ORM\Column(type="string", length=255, nullable=true)
     *
     * @var string
     */
    private $imageName;

    /**
     * @ORM\Column(type="integer", nullable=true)
     *
     * @var integer
     */
    private $imageSize;

    /**
     * @ORM\Column(type="datetime", nullable=true)
     *
     * @var \DateTime
     */
    private $updatedAt;

    /**
     * @var string
     *
     * @Gedmo\Slug(fields={"nom","prenoms","matricule"})
     * @ORM\Column(name="slug", type="string", length=255)
     */
    private $slug;

    /**
     * @var string
     *
     * @Gedmo\Blameable(on="create")
     * @ORM\Column(name="publie_par", type="string", length=25, nullable=true)
